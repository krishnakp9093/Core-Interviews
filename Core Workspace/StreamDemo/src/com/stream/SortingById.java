package com.stream;

public class SortingById {
	
	//attributes
	int id;
	String name;
	
	
	//parameterized constructor
	public SortingById(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	
	//getter setters
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
